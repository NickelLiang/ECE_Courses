; This program can print a String!.
; The String being print should be located in x5002.
; Nickel Liang, zuodong2.
; I used three iteration: row, charater, column.
; we have 16 row, unknown number of chars, 8 column of each char
; so mostly same as lab13, just add a char counter, and check if 
; next char is a null. That's it! Simple.
		.ORIG	x3000
	
				; R0: Reserved for output / Temporary
				; R1: Content to be print / Temporary
				; R2: Mask / Loop Condition Check / Temporary
				; R3: Row Counter
				; R4: Column Counter / Temporary
				; R5: Char Counter
				; R6: Content of M[R5]				

	; Row Loop
		AND R3, R3, #0	; set row counter to 0

NEXT_ROW	ADD R3, R3, #0	; R3 = 16 ?
		LD R2, ROW_LIMIT
		ADD R2, R2, R3
		BRz DONE

		AND R5, R5, #0	; set char counter to 0

	; Char Loop
NEXT_CHAR	LEA R4, FONT_DATA
		LD R1, PRINT_STR	; Load the starting addr of the string
		ADD R1, R1, R5	; R5 is the char offset, finish a char, R5 + 1
		LDR R6, R1, #0	; ASCII of the char in R6
		LD R2, MASK_CHAR
		AND R0, R6, R2	; R6 = 0 ? char = Nul?
		BRz DONE_ROW

		ADD R5, R5, #1
		AND R2, R2, #0
		ADD R2, R2, #4

ADDR_FINDER	ADD R2, R2, #0	; R2 = 0 ?
		BRz BEGIN_COLUMN

		ADD R6, R6, R6
		ADD R2, R2, #-1
		BRnzp ADDR_FINDER

BEGIN_COLUMN	ADD R6, R6, R4	; Starting addr of that char, add row # in R3
		ADD R6, R6, R3	; Now starting addr should be in R6
		
		LDR R0, R6, #0	; Now M[R6] is in R0
		AND R6, R6, #0
		ADD R6, R6, R0	; Pass content to R6
		LD R2, MASK	; x8000 mask to filter the first digit
		AND R4, R4, #0	; set column counter to 8
		ADD R4, R4, #8 
	
	; Column Loop
NEXT_COLUMN	ADD R4, R4, #0	; R4 != 0 ?
		BRz NEXT_CHAR
		
		AND R1, R6, R2	; Read One Digit from that Row
		BRn ONE
	
	; If 0, print content in x5000
		LD R1, ZERO_CHAR
		LDR R0, R1, #0
		OUT
		BRnzp PRINTED

	; If 1, print content in x5001
ONE		LD R1, ONE_CHAR
		LDR R0, R1, #0
		OUT

PRINTED		ADD R6, R6, R6	; Left Shift, so we can read the second digit
		
		ADD R4, R4, #-1 ; decrement column counter and move to next
		BRnzp NEXT_COLUMN

	; print new line char			
DONE_ROW	LD R0, ASCII_NL ; load NewLine ASCII value
		OUT
		
		ADD R3, R3, #1	; move to next row
		BRnzp NEXT_ROW

DONE		HALT

ASCII_NL	.FILL 	xA
MASK		.FILL	x8000
MASK_CHAR	.FILL	xFFFF
ZERO_CHAR	.FILL	x5000
ONE_CHAR	.FILL	x5001
PRINT_STR	.FILL	x5002
ROW_LIMIT	.FILL	xFFF0	; #-16

